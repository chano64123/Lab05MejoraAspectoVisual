package com.example.lab05mejoraaspectovisual;

public class Futbolista {
    String nombre;
    String edad;
    String club;
    String seleccion;
    String info;
    String link;
    int foto;

    public Futbolista(String nombre, String edad, String club, String seleccion, String info, String link, int foto) {
        this.nombre = nombre;
        this.edad = edad;
        this.club = club;
        this.seleccion = seleccion;
        this.info = info;
        this.link = link;
        this.foto = foto;
    }
}
